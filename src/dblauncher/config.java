    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String path = System.getProperty("user.home") + File.separator + "Documents" + File.separator;
        File file = new File(path+"myConfig.properties");
        try {
            prop = config.loadConfig();
            String keyword;
            String repl = jTextField2.getText();
            keyword = repl.replace("\\s+", " ");
            if(jTextField2.getText() != "" || jTextField2.getText() != ""){
            prop.setProperty(keyword, pathfile);
            prop.store(new FileOutputStream(file), null);
           
            this.dispose();
            //System.out.println(prop.elements());
            } else{JOptionPane.showMessageDialog(jTextField2, "Atalho est√° vazio!");}
            
        } catch (IOException ex) {
            Logger.getLogger(config.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

